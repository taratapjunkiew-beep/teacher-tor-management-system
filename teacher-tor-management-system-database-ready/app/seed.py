def seed_admin(db: Session):
    admin = db.query(User).filter(User.username == ADMIN_USERNAME).first()
    if not admin:
        db.add(User(username=ADMIN_USERNAME, full_name="ผู้ดูแลระบบ", password_hash=hash_password(ADMIN_PASSWORD), role="admin", is_active=True))
        db.commit()

